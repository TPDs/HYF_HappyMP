package com.company;

public class Point {
    private int h, v;

    public Point(int h, int v) {
        this.h = h;
        this.v = v;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }
}
